package bit.java39.test.step2;

public class Calculator {
	static double result;
	
	static public void plus(double a) {
		result = result + a;
	}
	
	static public void minus(double a) {
		result = result - a;
	}
	
	static public void multiple(double a) {
		result = result * a;
	}
	
	static public void divide(double a) {
		result = result / a;
	}
}
